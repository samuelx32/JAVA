package model.persistence;

/**
 *
 * @author samue
 */
public class Corrida {
    private String nome;
    private String rota;
    private String data_corrida;
    private String hora_corrida;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRota() {
        return rota;
    }

    public void setRota(String rota) {
        this.rota = rota;
    }

    public String getData_corrida() {
        return data_corrida;
    }

    public void setData_corrida(String data_corrida) {
        this.data_corrida = data_corrida;
    }

    public String getHora_corrida() {
        return hora_corrida;
    }

    public void setHora_corrida(String hora_corrida) {
        this.hora_corrida = hora_corrida;
    }
    
    
}
